// Real course data for AI/ML, DSA, and DevOps tracks
// Each node represents a real topic with actual prerequisite relationships

export interface CourseNode {
  id: string;
  label: string;
  difficulty: number; // 1-10
  estimatedHours: number;
  prerequisites: string[];
  description: string;
  courseUrl: string;
  coursePlatform: string;
  category: string;
  skills: string[];
}

export interface CourseEdge {
  source: string;
  target: string;
  weight: number; // difficulty delta
}

export interface Track {
  id: string;
  name: string;
  description: string;
  icon: string;
  nodes: CourseNode[];
  edges: CourseEdge[];
}

